class Solution {
    public int minDistance(String word1, String word2) {
        int m = word1.length(), n = word2.length();
        int[][] dp = new int[m][n];
        for (int[] row : dp) Arrays.fill(row, -1);
        return solve(word1, word2, 0, 0, dp);
    }

    private int solve(String a, String b, int i, int j, int[][] dp) {
        if (i == a.length() || j == b.length())
            return (a.length() - i) + (b.length() - j);

        if (dp[i][j] != -1) return dp[i][j];

        if (a.charAt(i) == b.charAt(j))
            return dp[i][j] = solve(a, b, i + 1, j + 1, dp);

        int insert = solve(a, b, i, j + 1, dp);
        int delete = solve(a, b, i + 1, j, dp);
        int replace = solve(a, b, i + 1, j + 1, dp);

        return dp[i][j] = 1 + Math.min(Math.min(insert, delete), replace);
    }
}
