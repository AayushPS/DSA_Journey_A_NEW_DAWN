public class EditDistance{
    public int minDistance(String word1, String word2) {
        int m = word1.length(), n = word2.length();

        int[] old = new int[n + 1];
        int[] now = new int[n + 1];

        // Base: converting "" â†’ word2[j..]
        for (int j = 0; j <= n; j++)
            old[j] = n - j;

        // Fill rows bottom-up
        for (int i = m - 1; i >= 0; i--) {

            // Base: converting word1[i..] â†’ ""
            now[n] = m - i;

            for (int j = n - 1; j >= 0; j--) {

                if (word1.charAt(i) == word2.charAt(j)) {
                    now[j] = old[j + 1];
                } else {
                    now[j] = 1 + Math.min(
                        Math.min(now[j + 1], old[j]),
                        old[j + 1]
                    );
                }
            }

            old = now;
            now = new int[n + 1];
        }

        return old[0];
    }
}


