public class CheckIfAllOnesAreAtLeastKPlacesAways {
    public boolean kLengthApart(int[] nums, int k) {
        int prev = -k - 1;    // ensures the first '1' is always valid

        for (int i = 0; i < nums.length; i++) {
            if (nums[i] == 1) {
                if (i - prev - 1 < k) return false; // too close
                prev = i; // update last '1' position
            }
        }
        return true;
    }
}


