public class CountOperationsToObtainZero {
    public int countOperations(int num1, int num2) {
        int operations = 0;

        // Keep performing subtraction until one becomes 0
        while (num1 != 0 && num2 != 0) {
            if (num1 >= num2) {
                num1 -= num2;  // subtract smaller from larger
            } else {
                num2 -= num1;
            }
            operations++;
        }

        return operations;
    }
}


